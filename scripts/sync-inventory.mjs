import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import {
  buildCategoryFields,
  getCabTypeFromCategories,
  normalizeManufacturerSlug,
} from "./lib/category-fields.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const sourcePath = path.join(root, "..", "products.json");
const outputPath = path.join(root, "src", "data", "inventory.ts");

function decodeHtml(text) {
  return text
    .replace(/&#(\d+);/g, (_, n) => String.fromCharCode(Number(n)))
    .replace(/&#x([0-9a-f]+);/gi, (_, h) => String.fromCharCode(parseInt(h, 16)))
    .replace(/&amp;/g, "&")
    .replace(/&mdash;/g, "—")
    .replace(/&ndash;/g, "–")
    .replace(/&#038;/g, "&")
    .replace(/&nbsp;/g, " ")
    .replace(/&#8242;/g, "'")
    .replace(/&#8243;/g, '"')
    .trim();
}

function stripTags(html) {
  return decodeHtml(html.replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim());
}

function parseSummaryTable(html) {
  const specs = {};
  const rowRegex = /<tr[^>]*>([\s\S]*?)<\/tr>/gi;
  let match;
  while ((match = rowRegex.exec(html)) !== null) {
    const cells = [...match[1].matchAll(/<t[dh][^>]*>([\s\S]*?)<\/t[dh]>/gi)].map((m) =>
      stripTags(m[1])
    );
    if (cells.length >= 2 && cells[0] && cells[1]) {
      specs[cells[0].toUpperCase()] = cells[1];
    }
  }
  return {
    vin: specs.VIN || "",
    year: specs.YEAR || specs["YEAR"] || "",
    manufacturer: specs.MANUFACTURER || "",
    model: specs.MODEL || "",
    miles: specs.MILES || "",
    hours: specs.HOURS || "",
    condition: specs.CONDITION || "",
  };
}

function parseDetailTable(html) {
  const details = {};
  const rowRegex = /<tr[^>]*>([\s\S]*?)<\/tr>/gi;
  let match;
  while ((match = rowRegex.exec(html)) !== null) {
    const cells = [...match[1].matchAll(/<t[dh][^>]*>([\s\S]*?)<\/t[dh]>/gi)].map((m) =>
      stripTags(m[1])
    );
    for (let i = 0; i < cells.length - 1; i += 2) {
      const key = cells[i];
      const value = cells[i + 1];
      if (key && value && !details[key]) {
        details[key] = value;
      }
    }
  }
  return details;
}

function getCategorySlugs(categories) {
  const slugs = categories.map((c) => c.slug);
  const type =
    slugs.find((s) => s === "sleeper-trucks") ||
    slugs.find((s) => s === "day-cabs") ||
    slugs.find((s) => s.includes("delivery-moving")) ||
    "other";
  return { type };
}

async function main() {
  let raw;
  if (fs.existsSync(sourcePath)) {
    raw = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
  } else {
    const res = await fetch("https://skltrucks.com/wp-json/wc/store/v1/products?per_page=50");
    raw = await res.json();
    fs.writeFileSync(sourcePath, JSON.stringify(raw, null, 2));
  }

  const products = raw.map((p) => {
    const summary = parseSummaryTable(p.short_description || "");
    const details = parseDetailTable(p.description || "");
    const cabType = getCabTypeFromCategories(p.categories || []) || getCategorySlugs(p.categories || []).type;
    const manufacturer = normalizeManufacturerSlug(summary.manufacturer || "");
    const { categories, categorySlugs } = buildCategoryFields(
      cabType === "other" ? "" : cabType,
      manufacturer
    );
    const uniqueImages = [...new Set((p.images || []).map((img) => img.src))];

    return {
      id: String(p.id),
      slug: p.slug,
      name: decodeHtml(p.name),
      price: Number(p.prices?.price || 0) / 100,
      image: uniqueImages[0] || "",
      images: uniqueImages,
      categories,
      categorySlugs,
      type: cabType === "other" ? "" : cabType,
      cabType: cabType === "other" ? "" : cabType,
      manufacturer,
      vin: summary.vin,
      year: summary.year,
      model: summary.model,
      miles: summary.miles,
      hours: summary.hours,
      condition: summary.condition,
      details,
    };
  });

  const content = `// Auto-generated by scripts/sync-inventory.mjs — run npm run sync-inventory to refresh
import type { Product } from "@/types/product";

export const products: Product[] = ${JSON.stringify(products, null, 2)};
`;

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, content);
  console.log(`Wrote ${products.length} products to ${outputPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
